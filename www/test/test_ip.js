        isIp('1.88.0.0', ['中国', '北京', '北京', '']);
        isIp('118.144.0.0', ['中国', '北京', '北京', '']);
        isIp('111.196.10.0', ['中国', '北京', '北京', '']);
        isIp('118.188.0.0', ['中国', '北京', '北京', '']);
        isIp('42.83.128.0', ['中国', '北京', '北京', '']);
        isIp('119.10.0.0', ['中国', '北京', '北京', '']);
        isIp('219.158.128.0', ['中国', '北京', '北京', '']);
